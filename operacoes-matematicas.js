// Operações matemáticas
// Adição
const numero1 = 5.5;
const numero2 = 3;
const resultadoDaSoma = numero1 + numero2;
console.log("Resultado da soma:", resultadoDaSoma); // 8.5

// Subtração
let numero3 = 10;
let numero4 = 4;
let resultadoDaSubtracao = numero3 - numero4;
console.log("Resultado da subtração:", resultadoDaSubtracao); // 6

// Multiplicação
const numero5 = 7;
let numero6 = 3;
const resultadoDaMultiplicacao = numero5 * numero6;
console.log("Resultado da multiplicação:", resultadoDaMultiplicacao); // 21

// Divisão
const numero7 = 20;
const numero8 = 4;
const resultadoDaDivisao = numero7 / numero8;
console.log("Resultado da divisão:", resultadoDaDivisao); // 5

// Módulo (resto da divisão)
const numero9 = 10;
const numero10 = 3;
const resultadoDoModulo = numero9 % numero10;
console.log("Resultado do módulo:", resultadoDoModulo); // 1

// Módulo (resto da divisão)
const numero91 = 10;
const numero101 = 2;
const resultadoDoModulo2 = numero91 % numero101;
console.log("Resultado do módulo:", resultadoDoModulo2); // 1